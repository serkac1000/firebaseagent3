
// GitHub Integration Test Script
// Run this to test GitHub API connectivity and functionality

const testGitHubIntegration = async () => {
  console.log('üöÄ Starting GitHub Integration Test...');
  
  // Test 1: Repository Import
  console.log('\nüì• Testing Repository Import...');
  try {
    const importResponse = await fetch('/api/github/import', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        repoUrl: 'https://github.com/octocat/Hello-World'
      }),
    });
    
    const importResult = await importResponse.json();
    console.log('‚úÖ Import test result:', importResult);
  } catch (error) {
    console.error('‚ùå Import test failed:', error);
  }
  
  // Test 2: GitHub Push (requires PAT)
  console.log('\nüì§ Testing GitHub Push...');
  const testPAT = process.env.GITHUB_PAT || 'your-pat-here';
  
  if (testPAT === 'your-pat-here') {
    console.log('‚ö†Ô∏è GitHub PAT not configured. Skipping push test.');
    console.log('   Set GITHUB_PAT environment variable to test push functionality.');
  } else {
    try {
      const pushResponse = await fetch('/api/github', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          repoUrl: 'https://github.com/your-username/test-repo',
          branch: 'main',
          commitMessage: 'Test commit from CodePilot',
          content: '# Test File\n\nThis is a test file generated by CodePilot GitHub integration.',
          pat: testPAT
        }),
      });
      
      const pushResult = await pushResponse.json();
      console.log('‚úÖ Push test result:', pushResult);
    } catch (error) {
      console.error('‚ùå Push test failed:', error);
    }
  }
  
  console.log('\nüèÅ GitHub Integration Test Complete!');
};

// Export for use in browser console or Node.js
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { testGitHubIntegration };
} else {
  // Browser environment
  window.testGitHubIntegration = testGitHubIntegration;
  console.log('GitHub test script loaded. Run testGitHubIntegration() to start tests.');
}
